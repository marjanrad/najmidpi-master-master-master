package com.example.najmidpi.model;

public class SensorObject {
    private String Date;
    private String Time;
    private String gamShomar;
    private String zarabaneGhalb;
    private String fesharSanj;
    private String vazn;

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        this.Date = date;
    }

    public String getGamShomar() {
        return gamShomar;
    }

    public void setGamShomar(String gamShomar) {
        this.gamShomar = gamShomar;
    }

    public String getZarabaneGhalb() {
        return zarabaneGhalb;
    }

    public void setZarabaneGhalb(String zarabaneGhalb) {
        this.zarabaneGhalb = zarabaneGhalb;
    }

    public String getFesharSanj() {
        return fesharSanj;
    }

    public void setFesharSanj(String fesharSanj) {
        this.fesharSanj = fesharSanj;
    }

    public String getVazn() {
        return vazn;
    }

    public void setVazn(String vazn) {
        this.vazn = vazn;
    }

    public String getTime() {
        return Time;
    }

    public void setTime(String time) {
        Time = time;
    }
}
